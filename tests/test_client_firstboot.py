class EnsureKernelDhcpResolverTests(unittest.TestCase):
    def test_ensure_kernel_dhcp_resolver_writes_resolv_conf_from_proc_net_pnp(self) -> None:
        logger = _FakeLogger()

        with tempfile.TemporaryDirectory() as tmpdir:
            root_dir = Path(tmpdir)
            resolv_path = root_dir / "etc" / "resolv.conf"
            resolv_path.parent.mkdir(parents=True)
            resolv_path.write_text("nameserver 8.8.8.8\n", encoding="utf-8")

            pnp_path = root_dir / "proc" / "net" / "pnp"
            pnp_path.parent.mkdir(parents=True)
            pnp_path.write_text(
                "#PROTO: DHCP\n"
                "domain home.nyanya.org\n"
                "nameserver 192.168.25.1\n"
                "nameserver 1.1.1.1\n",
                encoding="utf-8",
            )

            target = ensure_kernel_dhcp_resolver(logger, root=root_dir)

            self.assertEqual(target, "/etc/resolv.conf")
            self.assertFalse(resolv_path.is_symlink())
            self.assertEqual(
                resolv_path.read_text(encoding="utf-8"),
                "# Generated by HA-PXE from kernel DHCP boot data\n"
                "search home.nyanya.org\n"
                "nameserver 192.168.25.1\n"
                "nameserver 1.1.1.1\n",
            )

    def test_ensure_kernel_dhcp_resolver_raises_when_nameserver_is_missing(self) -> None:
        logger = _FakeLogger()

        with tempfile.TemporaryDirectory() as tmpdir:
            root_dir = Path(tmpdir)
            resolv_path = root_dir / "etc" / "resolv.conf"
            resolv_path.parent.mkdir(parents=True)
            resolv_path.write_text("nameserver 8.8.8.8\n", encoding="utf-8")
            pnp_path = root_dir / "proc" / "net" / "pnp"
            pnp_path.parent.mkdir(parents=True)
            pnp_path.write_text("domain home.nyanya.org\n", encoding="utf-8")

            with self.assertRaisesRegex(RuntimeError, "did not provide any nameserver entries"):
                ensure_kernel_dhcp_resolver(logger, root=root_dir)

            self.assertFalse(resolv_path.is_symlink())
            self.assertEqual(resolv_path.read_text(encoding="utf-8"), "nameserver 8.8.8.8\n")

    def test_repair_kernel_dhcp_resolver_refreshes_empty_resolv_conf(self) -> None:
        logger = _FakeLogger()

        with tempfile.TemporaryDirectory() as tmpdir:
            root_dir = Path(tmpdir)
            resolv_path = root_dir / "etc" / "resolv.conf"
            resolv_path.parent.mkdir(parents=True)
            resolv_path.write_text("# Managed by HA-PXE; populated from /proc/net/pnp on first boot\n", encoding="utf-8")

            pnp_path = root_dir / "proc" / "net" / "pnp"
            pnp_path.parent.mkdir(parents=True)
            pnp_path.write_text(
                "#PROTO: DHCP\n"
                "domain home.nyanya.org\n"
                "nameserver 192.168.25.1\n",
                encoding="utf-8",
            )

            repaired = repair_kernel_dhcp_resolver_if_needed(logger, root=root_dir)

            self.assertTrue(repaired)
            self.assertIn(
                "warn:/etc/resolv.conf does not contain any nameserver entries; refreshing it from kernel DHCP boot data",
                logger.messages,
            )
            self.assertEqual(
                resolv_path.read_text(encoding="utf-8"),
                "# Generated by HA-PXE from kernel DHCP boot data\n"
                "search home.nyanya.org\n"
                "nameserver 192.168.25.1\n",
            )

    def test_repair_kernel_dhcp_resolver_refreshes_loopback_only_resolver(self) -> None:
        logger = _FakeLogger()

        with tempfile.TemporaryDirectory() as tmpdir:
            root_dir = Path(tmpdir)
            resolv_path = root_dir / "etc" / "resolv.conf"
            resolv_path.parent.mkdir(parents=True)
            resolv_path.write_text("nameserver ::1\n", encoding="utf-8")

            pnp_path = root_dir / "proc" / "net" / "pnp"
            pnp_path.parent.mkdir(parents=True)
            pnp_path.write_text(
                "#PROTO: DHCP\n"
                "nameserver 192.168.25.1\n",
                encoding="utf-8",
            )

            repaired = repair_kernel_dhcp_resolver_if_needed(logger, root=root_dir)

            self.assertTrue(repaired)
            self.assertIn(
                "warn:/etc/resolv.conf only contains loopback nameserver entries; refreshing it from kernel DHCP boot data",
                logger.messages,
            )
            self.assertEqual(
                resolv_path.read_text(encoding="utf-8"),
                "# Generated by HA-PXE from kernel DHCP boot data\n"
                "nameserver 192.168.25.1\n",
            )

    def test_repair_kernel_dhcp_resolver_leaves_working_nameserver_unchanged(self) -> None:
        logger = _FakeLogger()

        with tempfile.TemporaryDirectory() as tmpdir:
            root_dir = Path(tmpdir)
            resolv_path = root_dir / "etc" / "resolv.conf"
            resolv_path.parent.mkdir(parents=True)
            resolv_path.write_text("nameserver 192.168.25.1\n", encoding="utf-8")

            repaired = repair_kernel_dhcp_resolver_if_needed(logger, root=root_dir)

            self.assertFalse(repaired)
            self.assertEqual(resolv_path.read_text(encoding="utf-8"), "nameserver 192.168.25.1\n")


